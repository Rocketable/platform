package main

import (
	"bytes"
	"crypto/sha256"
	"encoding/hex"
	"fmt"
	"go/format"
	"log"
	"os"
	"os/exec"
	"path/filepath"
	"strings"
)

func main() {
	log.SetPrefix("aesgcm8: ")
	log.SetFlags(0)
	log.SetOutput(os.Stderr)

	goEnv := exec.Command("go", "env", "GOROOT")

	goRootBytes, err := goEnv.Output()
	if err != nil {
		log.Fatalf("go env GOROOT: %v", err)
	}

	goroot := strings.TrimSpace(string(goRootBytes))
	if goroot == "" {
		log.Fatalf("go env GOROOT returned empty output")
	}

	sources := []string{
		"crypto/cipher/gcm.go",
		"crypto/internal/fips140/aes/gcm/ghash.go",
		"crypto/internal/fips140/alias/alias.go",
	}

	sourceNotes := make([]string, 0, len(sources))

	texts := map[string]string{}

	for _, name := range sources {
		path := filepath.Join(goroot, "src", filepath.FromSlash(name))

		data, err := os.ReadFile(path)
		if err != nil {
			log.Fatalf("read %s: %v", path, err)
		}

		checkSourceShape(name, data)
		texts[name] = string(data)
		sum := sha256.Sum256(data)
		sourceNotes = append(sourceNotes, fmt.Sprintf("// Source: %s sha256=%s", name, hex.EncodeToString(sum[:])))
	}

	versionCmd := exec.Command("go", "version")

	versionBytes, err := versionCmd.Output()
	if err != nil {
		log.Fatalf("go version: %v", err)
	}

	out := "gcm_gen.go"
	if err := os.WriteFile(out, []byte(generatedSource(strings.TrimSpace(string(versionBytes)), sourceNotes, texts)), 0o644); err != nil {
		log.Fatalf("write draft: %v", err)
	}

	rewrites := []string{
		"minTagSize() -> 8",
	}
	for _, rewrite := range rewrites {
		cmd := exec.Command("gofmt", "-w", "-r", rewrite, out)
		cmd.Stdout = os.Stdout

		cmd.Stderr = os.Stderr
		if err := cmd.Run(); err != nil {
			log.Fatalf("gofmt rewrite %q: %v", rewrite, err)
		}
	}

	data, err := os.ReadFile(out)
	if err != nil {
		log.Fatalf("read rewritten source: %v", err)
	}

	if bytes.Contains(data, []byte("minTagSize()")) {
		log.Fatalf("gofmt rewrite did not replace minTagSize")
	}

	formatted, err := format.Source(data)
	if err != nil {
		log.Fatalf("format generated source: %v", err)
	}

	if err := os.WriteFile(out, formatted, 0o644); err != nil {
		log.Fatalf("write generated source: %v", err)
	}
}

func checkSourceShape(name string, data []byte) {
	text := string(data)

	required := map[string][]string{
		"crypto/cipher/gcm.go": {
			"gcmMinimumTagSize    = 12",
			"func (g *gcmFallback) Open",
			"func gcmCounterCryptGeneric",
			"func gcmAuth",
		},
		"crypto/internal/fips140/aes/gcm/ghash.go": {
			"func GHASH",
			"func ghash(",
			"func ghashMul",
		},
		"crypto/internal/fips140/alias/alias.go": {
			"func AnyOverlap",
			"func InexactOverlap",
		},
	}
	for _, marker := range required[name] {
		if !strings.Contains(text, marker) {
			log.Fatalf("%s no longer contains marker %q", name, marker)
		}
	}
}

func generatedSource(goVersion string, sourceNotes []string, texts map[string]string) string {
	gcmBody := extractGCMFallback(texts["crypto/cipher/gcm.go"])
	ghashBody := extractGHASH(texts["crypto/internal/fips140/aes/gcm/ghash.go"])
	aliasBody := extractAlias(texts["crypto/internal/fips140/alias/alias.go"])

	return fmt.Sprintf(`// Copyright 2024 The Go Authors. All rights reserved.
// Use of this source code is governed by a BSD-style
// license that can be found in the LICENSE file.

// Code generated by go generate ./internal/discordvoice/internal/aesgcm8; DO NOT EDIT.
// Derived from the Go standard library GCM implementation in %s.
%s

package aesgcm8

import (
	"crypto/cipher"
	"crypto/subtle"
	"encoding/binary"
	"errors"
	"unsafe"
)

const (
	// TagSize is the 64-bit AES-GCM tag size used by Discord DAVE media frames.
	TagSize = 8

	gcmBlockSize         = 16
	gcmStandardNonceSize = 12
	gcmTagSize           = 16
	gcmMinimumTagSize    = minTagSize()
)

// New returns AES-GCM with a 96-bit nonce and Discord DAVE's 64-bit tag.
func New(block cipher.Block) (cipher.AEAD, error) {
	return newGCM(block, gcmStandardNonceSize, TagSize)
}

func newGCM(block cipher.Block, nonceSize, tagSize int) (cipher.AEAD, error) {
	if tagSize < gcmMinimumTagSize || tagSize > gcmBlockSize {
		return nil, errors.New("aesgcm8: incorrect tag size given to GCM")
	}
	if nonceSize <= 0 {
		return nil, errors.New("aesgcm8: the nonce can't have zero length")
	}
	if block.BlockSize() != gcmBlockSize {
		return nil, errors.New("aesgcm8: New requires 128-bit block cipher")
	}
	return &gcm{block: block, nonceSize: nonceSize, tagSize: tagSize}, nil
}

%s

%s

%s
`, goVersion, strings.Join(sourceNotes, "\n"), gcmBody, ghashBody, aliasBody)
}

func extractGCMFallback(text string) string {
	body := between(text, "// gcmAble is an interface", "func deriveCounter")
	body = after(body, "type gcmFallback struct")
	body += between(text, "func deriveCounter", "// sliceForAppend")
	body += between(text, "// sliceForAppend", "\n}") + "\n}"
	replacements := []struct{ old, new string }{
		{"gcmFallback", "gcm"},
		{"cipher    Block", "block     cipher.Block"},
		{"g.cipher", "g.block"},
		{"gcmCounterCryptGeneric(b Block", "gcmCounterCryptGeneric(b cipher.Block"},
		{"alias.InexactOverlap", "inexactOverlap"},
		{"alias.AnyOverlap", "anyOverlap"},
		{"byteorder.BEPutUint64", "binary.BigEndian.PutUint64"},
		{"byteorder.BEUint64", "binary.BigEndian.Uint64"},
		{"byteorder.BEPutUint32", "binary.BigEndian.PutUint32"},
		{"byteorder.BEUint32", "binary.BigEndian.Uint32"},
		{"gcm.GHASH", "ghashAll"},
		{"crypto/cipher: ", "aesgcm8: "},
		{"cipher: ", "aesgcm8: "},
	}

	return replaceAll(body, replacements)
}

func extractGHASH(text string) string {
	body := after(text, "type gcmFieldElement struct")
	body = strings.ReplaceAll(body, "func GHASH(key *[16]byte, inputs ...[]byte) []byte {\n\tfips140.RecordNonApproved()", "func ghashAll(key *[16]byte, inputs ...[]byte) []byte {")
	body = strings.ReplaceAll(body, "// GHASH is exposed to allow crypto/cipher to implement non-AES GCM modes.\n// It is not allowed as a stand-alone operation in FIPS mode because it\n// is not ACVP tested.\n", "")
	replacements := []struct{ old, new string }{
		{"byteorder.BEPutUint64", "binary.BigEndian.PutUint64"},
		{"byteorder.BEUint64", "binary.BigEndian.Uint64"},
		{"crypto/cipher", "aesgcm8"},
		{"FIPS mode", "this package"},
	}

	return replaceAll(body, replacements)
}

func extractAlias(text string) string {
	body := strings.TrimSpace(after(text, "func AnyOverlap"))
	replacements := []struct{ old, new string }{
		{"func AnyOverlap", "func anyOverlap"},
		{"func InexactOverlap", "func inexactOverlap"},
		{"return AnyOverlap", "return anyOverlap"},
		{"// InexactOverlap", "// inexactOverlap"},
		{"InexactOverlap can", "inexactOverlap can"},
	}

	return replaceAll(body, replacements)
}

func between(text, start, end string) string {
	startIndex := strings.Index(text, start)
	if startIndex < 0 {
		log.Fatalf("missing start marker %q", start)
	}

	endIndex := strings.Index(text[startIndex:], end)
	if endIndex < 0 {
		log.Fatalf("missing end marker %q", end)
	}

	return text[startIndex : startIndex+endIndex]
}

func after(text, marker string) string {
	index := strings.Index(text, marker)
	if index < 0 {
		log.Fatalf("missing marker %q", marker)
	}

	return text[index:]
}

func replaceAll(text string, replacements []struct{ old, new string }) string {
	for _, replacement := range replacements {
		text = strings.ReplaceAll(text, replacement.old, replacement.new)
	}

	return text
}
